## This Program will display a star that has 36 degrees angles.
import turtle

# user input
l_star = float(input("Enter desired length of a star:"))

# Draw the star
turtle.forward(l_star)

turtle.right(144)
turtle.forward(l_star)

turtle.right(144)
turtle.forward(l_star)

turtle.right(144)
turtle.forward(l_star)

turtle.right(144)
turtle.forward(l_star)

turtle.hideturtle()

turtle.done()





